Use Kowsi;
CREATE TABLE addresses (
    address_id INT,
    address varchar(255),
    CONSTRAINT addresses_pkey PRIMARY KEY (address_id)
);
CREATE TABLE categories (
    category_id INT NOT NULL,
    category_name VARCHAR(255) NOT NULL,
    CONSTRAINT categories_category_name_key UNIQUE (category_name),
    CONSTRAINT categories_pkey PRIMARY KEY (category_id)
    );
    CREATE TABLE customers (
    customer_id INT NOT NULL,
    customer_name VARCHAR(255) NOT NULL,
    CONSTRAINT customers_pkey PRIMARY KEY (customer_id)
);
CREATE TABLE departments (
    department_id INT  NOT NULL,
    department_name VARCHAR(255) NOT NULL,
    CONSTRAINT departments_department_name_key UNIQUE (department_name),
    CONSTRAINT departments_pkey PRIMARY KEY (department_id)
);
CREATE TABLE emails (
    email_id INT NOT NULL,
    email VARCHAR(255) NOT NULL,
    `domain` VARCHAR(255) NOT NULL,
    CONSTRAINT emails_pkey PRIMARY KEY (email_id)
);
CREATE TABLE employees (
    employee_id INT NOT NULL,
    first_name VARCHAR(255) NOT NULL,
    last_name VARCHAR(255) NOT NULL,
    department_id INT NOT NULL,
    CONSTRAINT employees_pkey PRIMARY KEY (employee_id)
);
CREATE TABLE orders (
    order_id INT NOT NULL,
    customer_id INT NULL,
    order_date DATE NOT NULL,
    amount DECIMAL(10, 2) NOT NULL,
    CONSTRAINT orders_pkey PRIMARY KEY (order_id)
);
CREATE TABLE project_tasks (
    task_id INT NOT NULL,
    project_id INT NULL,
    employee_id INT NULL,
    task_name VARCHAR(255) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    status VARCHAR(50) NOT NULL,
    CONSTRAINT project_tasks_pkey PRIMARY KEY (task_id),
    CONSTRAINT project_tasks_status_check CHECK (status IN ('Completed', 'Pending'))
);
CREATE TABLE salaries (
    employee_id INT NOT NULL,
    salary DECIMAL(10, 2) NOT NULL,
    from_date DATE NOT NULL,
    to_date DATE NOT NULL,
    CONSTRAINT salaries_pkey PRIMARY KEY (employee_id, from_date)
);
CREATE TABLE products (
    product_id INT NOT NULL,
    product_name VARCHAR(255) NOT NULL,
    category_id INT NULL,
    CONSTRAINT products_pkey PRIMARY KEY (product_id),
    CONSTRAINT products_category_id_fkey FOREIGN KEY (category_id) REFERENCES categories(category_id)
);
CREATE TABLE projects (
    project_id INT NOT NULL,
    employee_id INT NULL,
    project_name VARCHAR(255) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    CONSTRAINT project_pkey PRIMARY KEY (project_id),
    CONSTRAINT projects_employee_id_fkey FOREIGN KEY (employee_id) REFERENCES employees(employee_id)
);
CREATE TABLE returns (
    return_id INT NOT NULL,
    order_id INT NULL,
    return_date DATE NOT NULL,
    amount DECIMAL(10, 2) NOT NULL,
    CONSTRAINT returns_pkey PRIMARY KEY (return_id),
    CONSTRAINT returns_order_id_fkey FOREIGN KEY (order_id) REFERENCES orders(order_id)
);
CREATE TABLE sales (
    sale_id INT NOT NULL,
    product_id INT NULL,
    sale_date DATE NOT NULL,
    quantity INT NOT NULL,
    amount DECIMAL(10, 2) NOT NULL,
    CONSTRAINT sales_pkey PRIMARY KEY (sale_id),
    CONSTRAINT sales_product_id_fkey FOREIGN KEY (product_id) REFERENCES products(product_id)
);
