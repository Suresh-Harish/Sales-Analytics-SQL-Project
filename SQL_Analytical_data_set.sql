    SELECT 
        c.category_id,
        c.category_name,
        p.product_id,
        p.product_name,
        SUM(s.amount) AS total_sales
    FROM 
        sales s
    JOIN 
        products p ON s.product_id = p.product_id
    JOIN 
        categories c ON p.category_id = c.category_id
    GROUP BY 
        c.category_id, c.category_name, p.product_id, p.product_name
) AS category_product_sales
WHERE (
    SELECT COUNT(*)
    FROM (
        SELECT 
            p2.product_id,
            SUM(s2.amount) AS total_sales
        FROM 
            sales s2
        JOIN 
            products p2 ON s2.product_id = p2.product_id
        WHERE 
            p2.category_id = category_product_sales.category_id
        GROUP BY 
            p2.product_id
        HAVING 
            SUM(s2.amount) > category_product_sales.total_sales
    ) AS higher_sales
) < 3
ORDER BY 
    category_name,
    total_sales DESC;
    
